// var, let y const
var edad = 30;
var edad = 40;
/* Problema: estamos declarando 2 veces la misma variable y le estamos cambiando su valor */
console.log(edad);

// let 
let altura = 150; // declaramos la variable
altura = 200; // cambiamos su valor
console.log(altura);

// const "Constantes"
const velocidad = 100;
velocidad = 200;
console.log(velocidad);